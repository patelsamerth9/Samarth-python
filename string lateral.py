s1='hello samarth'# single quote string
s2="hello samarth"# double quote string
s3='''hello samarth'''# triple single quote string
s4="""hello samarth"""# triple double quote string
s5="""hello samarth"""# triple double quote string
print(s1)#In python we can use single,double,triple single and triple double quotes to define string
print(s2)#we can use any of them as per our convenience
print(s3)#Triple single quotes can be used for multi-line strings
print(s4)#Triple double quotes can also be used for multi-line strings
print(s5)#we can write multi-line string in any of the triple quotes